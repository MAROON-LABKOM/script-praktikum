void main(List<String> arguments) {
  var numberFirst = 10;
  var numberSecond = 5;
 
  var sumNumber = numberFirst + numberSecond;
  var minNumber = numberFirst - numberSecond;
  var multiNumber = numberFirst * numberSecond;
  var divNumber = numberFirst / numberSecond;
  var notNumberFirst = ~numberFirst;
  var modNumber = numberFirst % numberSecond;
 
  print(sumNumber);
  print(minNumber);
  print(multiNumber);
  print(divNumber);
  print(notNumberFirst);
  print(modNumber);
}
